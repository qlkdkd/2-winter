package c4_1_if_else;

public class c4_1_1_comparator {
    public static void main(String[]args){
        int x=3;
        int y=2;
        System.out.println(x<y);//false
        System.out.println(x>y);//true
        System.out.println(x==y);//false
        System.out.println(x!=y);//true
    }
}
